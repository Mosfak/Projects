    private boolean alphaNum(String s){
       int charter=0, num=0;
       for(int i=0;i<s.length();i++)
       {
           char c=s.charAt(i);
           if((c>='A' && c<='Z') || (c>='a' && c<='z') )
               charter=1;
           else if(c>='0' && c<='9')
               num=1; 
           else
               return true;

       }
       return !(charter==1 && num==1);

   }
    
