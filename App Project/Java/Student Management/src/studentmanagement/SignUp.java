    private void ButtonSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSignupActionPerformed
        try {
            // TODO add your handling code here:
            String Name= NameText.getText();
            String Email= EmailText.getText();
            String Pass= String.valueOf(PassField.getPassword());
            String Phone = PhoneField.getText();
            String Address = AddressField.getText();
            
            if(Pass.length()<8){
                JOptionPane.showMessageDialog(null, "Passward Must be 8 characters Long!!");
                this.dispose();
                this.setVisible(true);
            }
            if(alphaNum(Pass)==true){
                JOptionPane.showMessageDialog(null, "Password Must be alpha-numeric");
                this.dispose();
                this.setVisible(true);
                this.setLocationRelativeTo(null);
            }
            if(Name.length()==0 || Email.length()==0|| Phone.length() ==0 || Address.length()==0){
                JOptionPane.showMessageDialog(null, "Invalid Entry!!!"
                        + "\nYou Can Not leave any Field Empty!!");
                this.dispose();
                this.setVisible(true);
                this.setLocationRelativeTo(null);
            }
            else{
                String query="INSERT INTO `StudentTable` (`StudentID`, `Name`, `Email`, `Password`, `Phone`, `Address`, `CGPA`)"
                    + " VALUES (NULL, ?, ?, ?, ?, ?, NULL);";
                PreparedStatement st= DBConnect.getConnection().prepareStatement(query);
                st.setString(1,Name);
                st.setString(2,Email);
                st.setString(3,Pass);
                st.setString(4,Phone);
                st.setString(5,Address);

                int i=st.executeUpdate();
                if(i==0)
                    JOptionPane.showMessageDialog(null,"SignUp Failed!!!");
                else{
                    JOptionPane.showMessageDialog(null,"SignUp SuccessFull!!");
                    new Login().setLocationRelativeTo(null);
                    new Login().setVisible(true);
                    this.dispose();
                 }
            }
            
            
        } catch (SQLException | ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null,"SignUp Failed"+ex.getMessage());  
        }
        
    }//GEN-LAST:event_ButtonSignupActionPerformed

